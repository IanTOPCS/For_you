	mov 0x50, #0x92
	mov 0x51, #0x45
	mov 0x52, #0x67
	mov 0x53, #0x76

  ;低位元放0x60、高位元放0x61，初始化清零
	mov 0x60, #0x00
	mov 0x61, #0x00
	clr a

  ;把之前低位元搬進 累加器 中add加完放回低位
  ;高位將ADD導致的進位與之前進位累加後放回
	mov a, 0x60
	add a, 0x50 ;指定位置0x50
	mov 0x60, a
	mov a, 0x61
	addc a, #0x00
	mov 0x61, a

	mov a, 0x60
	add a, 0x51 ;指定位置0x51
	mov 0x60, a
	mov a, 0x61
	addc a, #0x00
	mov 0x61, a

	mov a, 0x60
	add a, 0x52 ;指定位置0x52
	mov 0x60, a
	mov a, 0x61
	addc a, #0x00
	mov 0x61, a

	mov a, 0x60
	add a, 0x53 ;指定位置0x53
	mov 0x60, a
	mov a, 0x61
	addc a, #0x00
	mov 0x61, a

	jmp $